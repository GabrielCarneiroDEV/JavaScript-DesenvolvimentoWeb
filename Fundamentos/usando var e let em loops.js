//mais um exemplo da utilização de var e let, nesse caso utilizando a estrutura de repetição for. No caso do var, é possível configurar a saída fora da estrutura de repetição, no let, não. 



for (var i = 0; i <=10; i++){
    console.log(i)
}
console.log("i = ", i)

//agora o let

for (let j = 0; j <=10; j++){
    console.log(j)
}
console.log("j = ", j)

